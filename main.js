ops = {
    add: function(a, b) {
        return a + b;
    },
    subtract: function(a, b) {
        return a - b;
    },
    multiply: function(a, b) {
        return a * b;
    },
    divide: function(a, b) {
        return a / b;
    }
}

console.log(ops.add(1,2));
console.log(ops.subtract(1,2));
console.log(ops.multiply(1,2));
console.log(ops.divide(1,2));
